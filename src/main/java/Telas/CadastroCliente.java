    private void btncadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncadastrarActionPerformed
    try {
        // 1. Captura os dados da UI
        Cliente cliente = new Cliente();
        cliente.setNome(txtnome.getText());
        cliente.setCpf(txtcpf.getText());
        cliente.setEndereco(txtendereco.getText());
        cliente.setTelefone(txttelefone.getText());

        // 2. Inicializa as camadas (DIP)
        jakarta.persistence.EntityManager em = infra.JPAUtil.getEntityManager();
        Entidades.ClienteJPA dao = new Entidades.ClienteJPA(em);
        Service.ClienteService service = new Service.ClienteService();

        // 3. Validação de Regras de Negócio (SRP)
        service.validarCliente(cliente);

        // 4. Persistência
        dao.cadastrarCliente(cliente);

        javax.swing.JOptionPane.showMessageDialog(this, "Cliente cadastrado com sucesso!");
        
        // Limpeza dos campos
        txtnome.setText("");
        txtcpf.setText("");
        txtendereco.setText("");
        txttelefone.setText("");

    } catch (IllegalArgumentException e) {
        // Captura erros de validação (campos vazios, CPF inválido)
        javax.swing.JOptionPane.showMessageDialog(this, e.getMessage());
    } catch (Exception ex) {
        // Captura erros de banco de dados
        javax.swing.JOptionPane.showMessageDialog(this, "Erro ao cadastrar: " + ex.getMessage());
    }
    }//GEN-LAST:event_btncadastrarActionPerformed

