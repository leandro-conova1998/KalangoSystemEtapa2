    private void btneditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditarActionPerformed
    if (pedido == null) return;

    try {
        // 1. Converter a String da tela para Date (Java)
        java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("dd/MM/yyyy");
        java.util.Date dataFormatada = sdf.parse(txtdata.getText());
        pedido.setData(dataFormatada);

        // 2. Atualizar o valor numérico
        pedido.setValor(Double.parseDouble(txtvalor.getText()));

        // 3. Salvar no banco
        new Entidades.PedidoJPA(em).editar(pedido);

        javax.swing.JOptionPane.showMessageDialog(this, "Pedido atualizado!");
        this.dispose();

    } catch (java.text.ParseException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Formato de data inválido! Use dd/MM/yyyy");
    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Valor numérico inválido!");
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
    }
    }//GEN-LAST:event_btneditarActionPerformed

