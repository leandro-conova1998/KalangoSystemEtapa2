    // 3. Método para colocar os dados do banco nos campos de texto
    private void preencherCampos() {
    if (pedido != null) {
        // Na sua classe Pedido, o método é getCliente().getNome() ou algo similar? 
        // Se o pedido for associado a um cliente, use:
        if (pedido.getCliente() != null) {
            txtnome.setText(pedido.getCliente().getNome());
        }
        
        // Formata a Data para exibir no campo de texto
        java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("dd/MM/yyyy");
        txtdata.setText(sdf.format(pedido.getData())); 
        
        // O nome correto na sua classe é getValor() e não getTotal()
        txtvalor.setText(String.valueOf(pedido.getValor())); 
    }
    }

